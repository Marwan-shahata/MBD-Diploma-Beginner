% Create a character array with my name
myName = 'marwan';

% Create another character array
FatherName = 'mohamed';

% Concatenate the two character arrays
fullName = [myName ,' ', FatherName];

% Display the results
disp(['My name: ' myName]);
disp(['Other name: ' FatherName]);
disp(['Full name: ' fullName]);